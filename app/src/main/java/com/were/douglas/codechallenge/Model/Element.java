package com.were.douglas.codechallenge.Model;

public class Element {


    private String name;
    private Double freq;
    private Double lib;
    private Double ai;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getFreq() {
        return freq;
    }

    public void setFreq(Double freq) {
        this.freq = freq;
    }

    public Double getLib() {
        return lib;
    }

    public void setLib(Double lib) {
        this.lib = lib;
    }

    public Double getAi() {
        return ai;
    }

    public void setAi(Double ai) {
        this.ai = ai;
    }
    public  Element(){}


}
